use demo;

create table INCIDENTS (
	IncidentNumber INT NOT NULL PRIMARY KEY,
	DateOfCall VARCHAR(50),
	CalYear INT,
	TimeOfCall TIME,
	HourOfCall INT,
	IncidentGroup VARCHAR(50),
	StopCodeDescription VARCHAR(50),
	SpecialServiceType VARCHAR(50),
    PropertyCategory VARCHAR(50),
    PropertyType VARCHAR(200),
    AddressQualifier VARCHAR(50),
    Postcode_full VARCHAR(50),
    Postcode_district VARCHAR(50),
    IncGeo_BoroughCode VARCHAR(50),
    IncGeo_BoroughName VARCHAR(50),
    ProperCase VARCHAR(50),
    IncGeo_WardCode VARCHAR(50),
    IncGeo_WardName VARCHAR(50),
    IncGeo_WardNameNew VARCHAR(50),
    Easting_m VARCHAR(50),
    Northing_m VARCHAR(50),
    Easting_rounded INT,
    Northing_rounded INT,
    FRS VARCHAR(50),
    IncidentStationGround VARCHAR(50),
    FirstPumpArriving_AttendanceTime VARCHAR(50),
    FirstPumpArriving_DeployedFromStation VARCHAR(50),
    SecondPumpArriving_AttendanceTime VARCHAR(50),
    SecondPumpArriving_DeployedFromStation VARCHAR(50),
    NumStationsWithPumpsAttending VARCHAR(50),
    NumPumpsAttending VARCHAR(50),
	CREATE_TS TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	UPDATE_TS TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);